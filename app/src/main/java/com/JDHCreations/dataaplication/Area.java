package com.JDHCreations.dataaplication;

public class Area {

    private int id;
    private String Name;

    public int getId() {
        return id;
    }

    public String getName() {
        return Name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        Name = name;
    }

    public Area(int id, String name) {
        this.id = id;
        Name = name;
    }

    public Area() {

    }




}
